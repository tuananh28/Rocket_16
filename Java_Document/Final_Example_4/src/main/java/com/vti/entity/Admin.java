package com.vti.entity;

public class Admin extends User{
	private int ExpInYear;

	public Admin(String fullName, String email, String password, Role role, int expInYear) {
		super(fullName, email, password, Role.ADMIN);
		ExpInYear = expInYear;
	}

	public int getExpInYear() {
		return ExpInYear;
	}

	public void setExpInYear(int expInYear) {
		ExpInYear = expInYear;
	}

	@Override
	public String toString() {
		return "Admin [ UserID =" + getUserID() + ",FullName =" + getFullName() + ", Email =" + getEmail() + ", Role =" + getRole() + ", ExpInYear = "+ ExpInYear + "]";
	}

	
	
}
